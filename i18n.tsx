interface I18nContextType {
    lang: Lang;
    setLang: (lang: Lang) => void;
    t: (key: string) => string;
}

const I18nContext = createContext<I18nContextType | undefined>(undefined);

export const I18nProvider = ({ children }: PropsWithChildren) => {
    const [lang, setLang] = useState<Lang>(() => {
        const saved = localStorage.getItem('gm_lang');
        return (saved as Lang) || 'en';
    });

    const changeLang = (newLang: Lang) => {
        setLang(newLang);
        localStorage.setItem('gm_lang', newLang);
        document.documentElement.lang = newLang;
    };

    const t = (key: string): string => {
        return translations[lang][key] || key;
    };

    return (
        <I18nContext.Provider value={{ lang, setLang: changeLang, t }}>
            {children}
        </I18nContext.Provider>
    );
};

export const useI18n = () => {
    const ctx = useContext(I18nContext);
    if (!ctx) throw new Error('useI18n must be used within I18nProvider');
    return ctx;
};

/**
 * Resolve a localized field from an object.
 * When lang is 'zh', returns item[field + '_zh'] if non-empty, otherwise falls back to item[field].
 */
export function localized(item: any, field: string, lang: string): string {
    if (lang === 'zh') {
        const zhVal = item?.[field + '_zh'];
        if (zhVal && typeof zhVal === 'string' && zhVal.trim()) return zhVal;
    }
    return item?.[field] || '';
}
